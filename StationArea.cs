namespace Roguelancer
{
    /// <summary>
    /// Represents the different areas/rooms within a docked space station
    /// </summary>
    public enum StationArea
    {
        /// <summary>
        /// Ship hangar where your ship is parked
        /// </summary>
        Hangar,
        
        /// <summary>
        /// Bar area for drinks and conversation
        /// </summary>
        Bar,
        
        /// <summary>
        /// Equipment and commodities dealer
        /// </summary>
        Dealer,
        
        /// <summary>
        /// Ship dealer for buying new ships
        /// </summary>
        ShipDealer
    }
}
